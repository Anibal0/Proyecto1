public class procesos {
  
    public int sumar (int n1, int n2){
        
        int resultado=0;
        
        resultado=n1+n2;
        
        return resultado;
        
        
    }
    public int restar (int n1, int n2){
        
        int resultado=0;
        
        resultado=n1-n2;
        
        return resultado;
        
    }
 
    public int multiplicacion (int n1, int n2){
        
        int resultado=0;
        
        resultado =n1 * n2;
        
        return resultado;
        
    }
    
    
    public int divicion (int n1, int n2){
        
        int resultado =0;
        
        resultado=n1/ n2;
        
        return resultado;
        
        
    }
    
    
    
    
}

    
   
